const MESSAGES: Record<DocferryLanguage, Messages> = {
  en: EN,
  "zh-CN": ZH_CN
};

export type { TranslationKey, TranslationValues };
export type Translate = (key: TranslationKey, values?: TranslationValues) => string;

export function isDocferryLanguage(value: unknown): value is DocferryLanguage {
  return value === "en" || value === "zh-CN";
}

export function nextLanguage(language: DocferryLanguage): DocferryLanguage {
  return language === "en" ? "zh-CN" : "en";
}

export function languageToggleKey(language: DocferryLanguage): TranslationKey {
  return language === "en" ? "language.toggleToZh" : "language.toggleToEn";
}

export function languageChangedKey(language: DocferryLanguage): TranslationKey {
  return language === "en" ? "language.changedToEn" : "language.changedToZh";
}

export function translate(
  language: DocferryLanguage,
  key: TranslationKey,
  values: TranslationValues = {}
): string {
  const template = MESSAGES[language][key] ?? EN[key];
  return template.replace(/\{(\w+)\}/g, (_match, name: string) => String(values[name] ?? ""));
}

export function makeTranslator(language: DocferryLanguage): Translate {
  return (key, values) => translate(language, key, values);
}
